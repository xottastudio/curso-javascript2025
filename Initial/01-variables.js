//var - primera forma para declarar variables, forma original, ahora ya NO se crea de esa manera
var helloWorld = 'hola mundo'
console.log(helloWorld)

var helloWorld = 'hola soy yo'
console.log(helloWorld)


//let, no necesita valor inicial, son accesibles solo dentro de un bloque

let helloWorld2 = 'hola mundo 2'
console.log(helloWorld2)

helloWorld2 = 'hola mundo 2, soy yo'
console.log(helloWorld2)

//const, se necesita un valor, es un valor constante y no se puede cambiar, no puede ser reasignado

const helloWorld3 = 'hola mundo 3'
console.log(helloWorld3)

//esto es un error
helloWorld3 = 'hola mundo 3, soy yo'
